/* Создание таблицы должников */

CREATE TABLE /*PREFIX*/DEBTORS
(
  DEBTOR_ID VARCHAR(32) NOT NULL,
  DEBTOR_TYPE INTEGER NOT NULL,
  SURNAME VARCHAR(100) NOT NULL,
  NAME VARCHAR(100) NOT NULL,
  PATRONYMIC VARCHAR(100) NOT NULL,
  PASSPORT VARCHAR(250) NOT NULL,
  SEX INTEGER NOT NULL,
  DATE_BIRTH DATETIME NOT NULL,
  PLACE_BIRTH VARCHAR(250) NOT NULL, 
  ADDRESS_RESIDENCE VARCHAR(250),
  INDEX_RESIDENCE VARCHAR(10),
  ADDRESS_ACTUAL VARCHAR(250),
  INDEX_ACTUAL VARCHAR(10),
  ADDRESS_ADDITIONAL VARCHAR(250),
  INDEX_ADDITIONAL VARCHAR(10),
  JOB_TITLE VARCHAR(250),
  PLACE_WORK VARCHAR(250),
  ADDRESS_WORK VARCHAR(250),
  INDEX_WORK VARCHAR(10),
  PHONE_HOME VARCHAR(20),
  PHONE_WORK VARCHAR(20),
  PHONE_MOBILE VARCHAR(20),
  PHONE_OTHER1 VARCHAR(20),
  PHONE_OTHER2 VARCHAR(20),
  FOUNDERS IMAGE,
  PRIMARY KEY (DEBTOR_ID)
)

--

/* Создание просмотра таблицы должников */

CREATE VIEW /*PREFIX*/S_DEBTORS
AS
SELECT * FROM /*PREFIX*/DEBTORS

--

/* Создание процедуры добавления должника */

CREATE PROCEDURE /*PREFIX*/I_DEBTOR
  @DEBTOR_ID VARCHAR(32),
  @DEBTOR_TYPE INTEGER,
  @SURNAME VARCHAR(100),
  @NAME VARCHAR(100),
  @PATRONYMIC VARCHAR(100),
  @PASSPORT VARCHAR(250),
  @SEX INTEGER,
  @DATE_BIRTH DATETIME,
  @PLACE_BIRTH VARCHAR(250), 
  @ADDRESS_RESIDENCE VARCHAR(250),
  @INDEX_RESIDENCE VARCHAR(10),
  @ADDRESS_ACTUAL VARCHAR(250),
  @INDEX_ACTUAL VARCHAR(10),
  @ADDRESS_ADDITIONAL VARCHAR(250),
  @INDEX_ADDITIONAL VARCHAR(10),
  @JOB_TITLE VARCHAR(250),
  @PLACE_WORK VARCHAR(250),
  @ADDRESS_WORK VARCHAR(250),
  @INDEX_WORK VARCHAR(10),
  @PHONE_HOME VARCHAR(20),
  @PHONE_WORK VARCHAR(20),
  @PHONE_MOBILE VARCHAR(20),
  @PHONE_OTHER1 VARCHAR(20),
  @PHONE_OTHER2 VARCHAR(20),
  @FOUNDERS IMAGE
AS
BEGIN
  INSERT INTO /*PREFIX*/DEBTORS (DEBTOR_ID,DEBTOR_TYPE,SURNAME,NAME,PATRONYMIC,PASSPORT,SEX,DATE_BIRTH,
                                 PLACE_BIRTH,ADDRESS_RESIDENCE,INDEX_RESIDENCE,ADDRESS_ACTUAL,INDEX_ACTUAL,
                                 ADDRESS_ADDITIONAL,INDEX_ADDITIONAL,JOB_TITLE,PLACE_WORK,ADDRESS_WORK,
                                 INDEX_WORK,PHONE_HOME,PHONE_WORK,PHONE_MOBILE,PHONE_OTHER1,PHONE_OTHER2,
                                 FOUNDERS)
       VALUES (@DEBTOR_ID,@DEBTOR_TYPE,@SURNAME,@NAME,@PATRONYMIC,@PASSPORT,@SEX,@DATE_BIRTH,
               @PLACE_BIRTH,@ADDRESS_RESIDENCE,@INDEX_RESIDENCE,@ADDRESS_ACTUAL,@INDEX_ACTUAL,
               @ADDRESS_ADDITIONAL,@INDEX_ADDITIONAL,@JOB_TITLE,@PLACE_WORK,@ADDRESS_WORK,
               @INDEX_WORK,@PHONE_HOME,@PHONE_WORK,@PHONE_MOBILE,@PHONE_OTHER1,@PHONE_OTHER2,
               @FOUNDERS);
END;

