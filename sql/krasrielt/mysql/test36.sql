SELECT V.NAME AS VIEW_NAME,
       T.NAME AS TYPE_NAME,

			 (SELECT COUNT(*) 
			    FROM PUBLISHING_OBJECTS PO 
			    JOIN OBJECTS O ON O.OBJECT_ID=PO.OBJECT_ID
				 WHERE O.VIEW_ID=VT.VIEW_ID AND O.TYPE_ID=VT.TYPE_ID 
				   AND O.OPERATION_ID='9BB38FBAEE8B88A54FD02C983AE5C607' AND O.ACCOUNT_ID='2BCC63C1D053A07D4DE66312ED77519D') AS SELL_AMOUNT,
					 
			 (SELECT COUNT(*) 
			    FROM PUBLISHING_OBJECTS PO 
			    JOIN OBJECTS O ON O.OBJECT_ID=PO.OBJECT_ID
				 WHERE O.VIEW_ID=VT.VIEW_ID AND O.TYPE_ID=VT.TYPE_ID 
				   AND O.OPERATION_ID='A7D600213026BBCE43F7B4676A8DC7F1' AND O.ACCOUNT_ID='2BCC63C1D053A07D4DE66312ED77519D') AS BUY_AMOUNT,

			 (SELECT COUNT(*) 
			    FROM PUBLISHING_OBJECTS PO 
			    JOIN OBJECTS O ON O.OBJECT_ID=PO.OBJECT_ID
				 WHERE O.VIEW_ID=VT.VIEW_ID AND O.TYPE_ID=VT.TYPE_ID 
				   AND O.OPERATION_ID='D5F0B39F9E48A9304EA0A28ABFA2132F' AND O.ACCOUNT_ID='2BCC63C1D053A07D4DE66312ED77519D') AS CHANGE_AMOUNT,

			 (SELECT COUNT(*) 
			    FROM PUBLISHING_OBJECTS PO 
			    JOIN OBJECTS O ON O.OBJECT_ID=PO.OBJECT_ID
				 WHERE O.VIEW_ID=VT.VIEW_ID AND O.TYPE_ID=VT.TYPE_ID 
				   AND O.OPERATION_ID='31B977EB8E80ABEB4BB4215FD6777072' AND O.ACCOUNT_ID='2BCC63C1D053A07D4DE66312ED77519D') AS DELIVER_AMOUNT,

			 (SELECT COUNT(*) 
			    FROM PUBLISHING_OBJECTS PO 
			    JOIN OBJECTS O ON O.OBJECT_ID=PO.OBJECT_ID
				 WHERE O.VIEW_ID=VT.VIEW_ID AND O.TYPE_ID=VT.TYPE_ID 
				   AND O.OPERATION_ID='8B5206B8E53DB842408847A0C46E0A1D' AND O.ACCOUNT_ID='2BCC63C1D053A07D4DE66312ED77519D') AS SHOOT_AMOUNT

	FROM VIEW_TYPES VT 
	JOIN VIEWS V ON V.VIEW_ID=VT.VIEW_ID
	JOIN TYPES T ON T.TYPE_ID=VT.TYPE_ID
 ORDER BY V.PRIORITY, VT.PRIORITY
 
 	 