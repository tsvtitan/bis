/* Создание процедуры получения третьего раунда */

CREATE PROCEDURE /*PREFIX*/GET_THIRD_ROUND
(
  TIRAGE_ID VARCHAR(32),
  PRIZE_ID VARCHAR(32)
)
RETURNS
(
  TICKET_COUNT INTEGER
)
AS
BEGIN
  TICKET_COUNT=0;

  SELECT COUNT(*)
    FROM /*PREFIX*/GET_TICKET_SERIES(:TIRAGE_ID,:PRIZE_ID,3)
    INTO :TICKET_COUNT;

--  SUSPEND;
END;

