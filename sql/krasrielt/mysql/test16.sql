SELECT OP.OBJECT_ID,
MIN(IF(OP.PARAM_ID='A753A13C38F2846842B225E8FE9608F5',CONVERT(OP.VALUE,DECIMAL(15,2)),NULL)) AS 'A753A13C38F2846842B225E8FE9608F5', /* Iauay ieiuaau */
MIN(IF(OP.PARAM_ID='93B2E581C50EAC7B4D4F7610B78639E9',CONVERT(OP.VALUE,DECIMAL(15,2)),NULL)) AS '93B2E581C50EAC7B4D4F7610B78639E9', /* ?eeay ieiuaau */
MIN(IF(OP.PARAM_ID='1B5C7024F91CA674453748230A848286',CONVERT(OP.VALUE,DECIMAL(15,2)),NULL)) AS '1B5C7024F91CA674453748230A848286',
CONCAT(CONVERT(MIN(IF(OP.PARAM_ID='A753A13C38F2846842B225E8FE9608F5',CONVERT(OP.VALUE,DECIMAL(15,2)),NULL)),CHAR),
       CONVERT(MIN(IF(OP.PARAM_ID='93B2E581C50EAC7B4D4F7610B78639E9',CONVERT(OP.VALUE,DECIMAL(15,2)),NULL)),CHAR),
       CONVERT(MIN(IF(OP.PARAM_ID='1B5C7024F91CA674453748230A848286',CONVERT(OP.VALUE,DECIMAL(15,2)),NULL)),CHAR)
          
)
	        FROM OBJECT_PARAMS OP
					JOIN OBJECTS O ON O.OBJECT_ID=OP.OBJECT_ID
				 WHERE OP.PARAM_ID IN (						 'A753A13C38F2846842B225E8FE9608F5',
															 '93B2E581C50EAC7B4D4F7610B78639E9',
															 '1B5C7024F91CA674453748230A848286')
           AND OP.DATE_CREATE=(SELECT MAX(DATE_CREATE) FROM OBJECT_PARAMS OP1
					                      WHERE OP1.PARAM_ID=OP.PARAM_ID AND OP1.OBJECT_ID=O.OBJECT_ID)
				 GROUP BY OP.OBJECT_ID
